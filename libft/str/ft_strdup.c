#include "../libft.h"

char	*ft_strdup(const char *s)
{
	int		len;
	int		i;
	char	*str;

	i = 0;
	len = ft_strlen((char *)s);
	str = (char *)new(sizeof(char), (len + 1));
	if (!str)
		return (NULL);
	while (i < len)
	{
		str[i] = s[i];
		i++;
	}
	str[i] = '\0';
	return (str);
}
